char *ft_strncat(char *dest, char *src, unsigned int nb)
{
	int	i;
	unsigned int	i_s;

	i = 0;
	i_s = 0;
	while (dest[i] != '\0')
		i++;
	while (i_s < nb && src[i_s] != '\0')
	{
		dest[i] = src[i_s];
		i++;
		i_s++;
	}
	dest[i] = '\0';
	return (dest);   
}