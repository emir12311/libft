char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	i_s;

	i = 0;
	i_s = 0;
	while (dest[i] != '\0')
		i++;
	while (src[i_s] != '\0')
	{
		dest[i] = src[i_s];
		i++;
		i_s++;
	}
	dest[i] = '\0';
	return (dest);
}
